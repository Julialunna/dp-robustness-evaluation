"""Local DP-CVAE used to synthesize private MNIST embeddings.

This module implements the first stage of the proposed pipeline:
real local embeddings -> local CVAE trained with DP-SGD -> synthetic embeddings.

The federated model is NOT the CVAE. The federated model is the downstream
classifier trained later on the synthetic embeddings generated by each client.
"""

from __future__ import annotations

from dataclasses import dataclass
from pathlib import Path
from typing import Literal, Optional, Tuple

import torch
import torch.nn as nn
import torch.nn.functional as F
from opacus import PrivacyEngine
from opacus.validators import ModuleValidator
from torch.utils.data import DataLoader, TensorDataset


class ConditionalVAE(nn.Module):
    """MLP Conditional VAE for vector embeddings.

    Input: embedding x in R^input_dim and class label y.
    Output: reconstructed/generated embedding in R^input_dim.
    """

    def __init__(
        self,
        input_dim: int,
        num_classes: int = 10,
        hidden_dim: int = 256,
        latent_dim: int = 32,
    ) -> None:
        super().__init__()
        self.input_dim = input_dim
        self.num_classes = num_classes
        self.latent_dim = latent_dim

        enc_in = input_dim + num_classes
        dec_in = latent_dim + num_classes

        self.encoder = nn.Sequential(
            nn.Linear(enc_in, hidden_dim),
            nn.ReLU(),
            nn.Linear(hidden_dim, hidden_dim),
            nn.ReLU(),
        )
        self.fc_mu = nn.Linear(hidden_dim, latent_dim)
        self.fc_logvar = nn.Linear(hidden_dim, latent_dim)

        self.decoder = nn.Sequential(
            nn.Linear(dec_in, hidden_dim),
            nn.ReLU(),
            nn.Linear(hidden_dim, hidden_dim),
            nn.ReLU(),
            nn.Linear(hidden_dim, input_dim),
            nn.Sigmoid(),
        )

    def encode(self, x: torch.Tensor, y: torch.Tensor) -> Tuple[torch.Tensor, torch.Tensor]:
        y_one_hot = F.one_hot(y, num_classes=self.num_classes).float()
        h = self.encoder(torch.cat([x, y_one_hot], dim=1))
        return self.fc_mu(h), self.fc_logvar(h)

    @staticmethod
    def reparameterize(mu: torch.Tensor, logvar: torch.Tensor) -> torch.Tensor:
        std = torch.exp(0.5 * logvar)
        eps = torch.randn_like(std)
        return mu + eps * std

    def decode(self, z: torch.Tensor, y: torch.Tensor) -> torch.Tensor:
        y_one_hot = F.one_hot(y, num_classes=self.num_classes).float()
        return self.decoder(torch.cat([z, y_one_hot], dim=1))

    def forward(self, x: torch.Tensor, y: torch.Tensor) -> Tuple[torch.Tensor, torch.Tensor, torch.Tensor]:
        mu, logvar = self.encode(x, y)
        z = self.reparameterize(mu, logvar)
        recon = self.decode(z, y)
        return recon, mu, logvar


def cvae_loss(
    recon: torch.Tensor,
    x: torch.Tensor,
    mu: torch.Tensor,
    logvar: torch.Tensor,
    beta: float = 1.0,
) -> torch.Tensor:
    """Mean CVAE loss with per-example reconstruction/KL before averaging.

    Opacus can compute per-sample gradients when the final scalar is the mean
    over per-example losses.
    """
    recon_per_sample = F.mse_loss(recon, x, reduction="none").sum(dim=1)
    kl_per_sample = -0.5 * torch.sum(1 + logvar - mu.pow(2) - logvar.exp(), dim=1)
    return (recon_per_sample + beta * kl_per_sample).mean()


@dataclass
class DPCVAETrainResult:
    model: ConditionalVAE
    epsilon: Optional[float]
    num_examples: int
    final_loss: float


def train_local_dp_cvae(
    embeddings: torch.Tensor,
    labels: torch.Tensor,
    *,
    input_dim: int,
    num_classes: int,
    latent_dim: int,
    hidden_dim: int,
    batch_size: int,
    epochs: int,
    lr: float,
    beta: float,
    use_dp: bool,
    target_epsilon: float,
    target_delta: float,
    max_grad_norm: float,
    device: torch.device,
) -> DPCVAETrainResult:
    """Train one local CVAE on one client's embeddings.

    This is local only: no CVAE weights are sent to the server.
    """
    dataset = TensorDataset(embeddings.float(), labels.long())
    loader = DataLoader(dataset, batch_size=batch_size, shuffle=True, drop_last=False)

    model = ConditionalVAE(
        input_dim=input_dim,
        num_classes=num_classes,
        hidden_dim=hidden_dim,
        latent_dim=latent_dim,
    )
    errors = ModuleValidator.validate(model, strict=False)
    if errors:
        model = ModuleValidator.fix(model)

    model.to(device)
    optimizer = torch.optim.Adam(model.parameters(), lr=lr)
    privacy_engine: Optional[PrivacyEngine] = None

    if use_dp:
        privacy_engine = PrivacyEngine(secure_mode=False)
        model, optimizer, loader = privacy_engine.make_private_with_epsilon(
            module=model,
            optimizer=optimizer,
            data_loader=loader,
            target_epsilon=target_epsilon,
            target_delta=target_delta,
            epochs=epochs,
            max_grad_norm=max_grad_norm,
        )

    final_loss = 0.0
    for _ in range(epochs):
        model.train()
        running_loss = 0.0
        running_examples = 0
        for x_batch, y_batch in loader:
            x_batch = x_batch.to(device)
            y_batch = y_batch.to(device)

            optimizer.zero_grad(set_to_none=True)
            recon, mu, logvar = model(x_batch, y_batch)
            loss = cvae_loss(recon, x_batch, mu, logvar, beta=beta)
            loss.backward()
            optimizer.step()

            batch_n = y_batch.size(0)
            running_loss += loss.item() * batch_n
            running_examples += batch_n
        final_loss = running_loss / max(running_examples, 1)

    epsilon = None
    if privacy_engine is not None:
        epsilon = float(privacy_engine.get_epsilon(delta=target_delta))

    # If Opacus wrapped the module, return the underlying plain model for generation/saving.
    plain_model = model._module if hasattr(model, "_module") else model
    plain_model.eval()

    return DPCVAETrainResult(
        model=plain_model,
        epsilon=epsilon,
        num_examples=len(dataset),
        final_loss=float(final_loss),
    )


@torch.no_grad()
def generate_synthetic_embeddings(
    model: ConditionalVAE,
    *,
    num_samples: int,
    num_classes: int,
    latent_dim: int,
    device: torch.device,
    label_mode: Literal["uniform", "real_distribution"] = "uniform",
    real_labels: Optional[torch.Tensor] = None,
) -> Tuple[torch.Tensor, torch.Tensor]:
    """Generate synthetic embeddings from the CVAE decoder.

    Important: this samples z and decodes it. It does NOT use encoder outputs
    mu(x_real), because those would still be direct functions of real examples.
    """
    model.to(device)
    model.eval()

    if label_mode == "real_distribution":
        if real_labels is None:
            raise ValueError("real_labels must be provided when label_mode='real_distribution'.")
        idx = torch.randint(0, len(real_labels), (num_samples,))
        labels = real_labels[idx].long().to(device)
    elif label_mode == "uniform":
        labels = torch.arange(num_samples, device=device) % num_classes
        labels = labels[torch.randperm(num_samples, device=device)]
    else:
        raise ValueError(f"Unknown label_mode: {label_mode}")

    z = torch.randn(num_samples, latent_dim, device=device)
    synthetic = model.decode(z, labels)
    return synthetic.cpu(), labels.cpu()


def save_synthetic_dataset(path: str | Path, embeddings: torch.Tensor, labels: torch.Tensor, metadata: dict) -> None:
    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    torch.save({"embeddings": embeddings.cpu(), "labels": labels.cpu(), "metadata": metadata}, path)


def load_synthetic_dataset(path: str | Path) -> Tuple[torch.Tensor, torch.Tensor, dict]:
    data = torch.load(path, map_location="cpu")
    return data["embeddings"], data["labels"], data.get("metadata", {})
