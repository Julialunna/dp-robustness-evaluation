import warnings
from pathlib import Path

import torch
from flwr.client import ClientApp, NumPyClient
from flwr.common import Context

import dp_cvae
import parameters_federated
import train

warnings.filterwarnings("ignore", category=UserWarning)


class FlowerClient(NumPyClient):


    def __init__(
        self,
        train_loader,
        test_loader,
        target_delta,
        noise_multiplier,
        max_grad_norm,
        partition_id,
        context,
    ) -> None:
        super().__init__()
        self.model = train.EmbeddingClassifier(
            input_size=parameters_federated.EMBEDDING_DIM,
            hidden_size=parameters_federated.EMBEDDING_HIDDEN_SIZE,
            num_classes=parameters_federated.NUM_CLASSES,
        )
        self.train_loader = train_loader
        self.test_loader = test_loader
        self.target_delta = target_delta
        self.noise_multiplier = noise_multiplier
        self.max_grad_norm = max_grad_norm
        self.partition_id = int(partition_id)
        self.context = context
        self.device = torch.device("cuda:0" if torch.cuda.is_available() else "cpu")

        # Same frozen MLP extractor on every client and on the server.
        self.embedding_model = train.build_embedding_extractor(self.device)

    def _synthetic_cache_path(self) -> Path:
        cache_dir = Path(parameters_federated.SYNTHETIC_CACHE_DIR)
        return cache_dir / f"client_{self.partition_id}_synthetic.pt"

    def _extractor_signature(self) -> dict:
        """Small signature used to avoid reusing stale synthetic-embedding caches."""
        path_str = getattr(parameters_federated, "EMBEDDING_EXTRACTOR_PATH", None)
        path = Path(path_str) if path_str else None
        sig = {
            "embedding_extractor_path": str(path) if path is not None else None,
            "embedding_dim": parameters_federated.EMBEDDING_DIM,
            "embedding_hidden_size": parameters_federated.EMBEDDING_HIDDEN_SIZE,
            "pretrain_ratio": parameters_federated.PRETRAIN_RATIO,
            "data_split_seed": parameters_federated.DATA_SPLIT_SEED,
        }
        if path is not None and path.exists():
            stat = path.stat()
            sig["extractor_mtime_ns"] = stat.st_mtime_ns
            sig["extractor_size_bytes"] = stat.st_size
        return sig

    def _expected_cache_metadata(self) -> dict:
        metadata = {
            "cache_version": parameters_federated.SYNTHETIC_CACHE_VERSION,
            "partition_id": self.partition_id,
            "num_real_train_examples": len(self.train_loader.dataset),
            "partitioner": parameters_federated.PARTITIONER,
            "dirichlet_alpha": parameters_federated.DIRICHLET_ALPHA,
            "synthetic_multiplier": parameters_federated.SYNTHETIC_MULTIPLIER,
            "label_mode": parameters_federated.SYNTHETIC_LABEL_MODE,
            "use_local_dp_cvae": parameters_federated.USE_LOCAL_DP_CVAE,
            "target_epsilon": parameters_federated.TARGET_EPSILON,
            "target_delta": self.target_delta,
            "max_grad_norm": self.max_grad_norm,
            "cvae_hidden_dim": parameters_federated.CVAE_HIDDEN_DIM,
            "cvae_latent_dim": parameters_federated.CVAE_LATENT_DIM,
            "cvae_epochs": parameters_federated.CVAE_EPOCHS,
            "cvae_beta": parameters_federated.CVAE_BETA,
        }
        metadata.update(self._extractor_signature())
        return metadata

    @staticmethod
    def _cache_is_valid(cached_metadata: dict, expected_metadata: dict) -> bool:
        for key, expected_value in expected_metadata.items():
            if cached_metadata.get(key) != expected_value:
                return False
        return True

    def _build_or_load_synthetic_loader(self):
        """Create/load synthetic embeddings generated by a local DP-CVAE."""
        cache_path = self._synthetic_cache_path()
        should_retrain = (
            parameters_federated.RETRAIN_CVAE_EVERY_ROUND
            or getattr(parameters_federated, "FORCE_REGENERATE_SYNTHETICS", False)
        )
        expected_metadata = self._expected_cache_metadata()

        if cache_path.exists() and not should_retrain:
            synthetic_embeddings, synthetic_labels, metadata = dp_cvae.load_synthetic_dataset(cache_path)
            if self._cache_is_valid(metadata, expected_metadata):
                epsilon = metadata.get("epsilon")
                cvae_loss = metadata.get("cvae_final_loss")
                print(
                    f"[Cliente {self.partition_id}] Sintéticos carregados do cache válido "
                    f"({len(synthetic_labels)} exemplos)."
                )
                synthetic_loader = train.make_embedding_loader(
                    synthetic_embeddings,
                    synthetic_labels,
                    batch_size=parameters_federated.BATCH_SIZE,
                    shuffle=True,
                )
                return synthetic_loader, epsilon, cvae_loss, len(synthetic_labels)

            print(
                f"[Cliente {self.partition_id}] Cache sintético antigo/incompatível. "
                "Regenerando com o extrator e os hiperparâmetros atuais."
            )

        print(f"[Cliente {self.partition_id}] Extraindo embeddings reais locais...")
        real_embeddings, real_labels = train.extract_embeddings_from_loader(
            self.embedding_model,
            self.train_loader,
            self.device,
        )

        print(f"[Cliente {self.partition_id}] Treinando CVAE local com DP-SGD...")
        result = dp_cvae.train_local_dp_cvae(
            real_embeddings,
            real_labels,
            num_classes=parameters_federated.NUM_CLASSES,
            input_dim=parameters_federated.EMBEDDING_DIM,
            hidden_dim=parameters_federated.CVAE_HIDDEN_DIM,
            latent_dim=parameters_federated.CVAE_LATENT_DIM,
            batch_size=parameters_federated.CVAE_BATCH_SIZE,
            epochs=parameters_federated.CVAE_EPOCHS,
            lr=parameters_federated.CVAE_LR,
            beta=parameters_federated.CVAE_BETA,
            use_dp=parameters_federated.USE_LOCAL_DP_CVAE,
            target_epsilon=parameters_federated.TARGET_EPSILON,
            target_delta=self.target_delta,
            max_grad_norm=self.max_grad_norm,
            device=self.device,
        )

        synthetic_embeddings, synthetic_labels = dp_cvae.generate_synthetic_embeddings(
            result.model,
            real_labels=real_labels,
            num_classes=parameters_federated.NUM_CLASSES,
            latent_dim=parameters_federated.CVAE_LATENT_DIM,
            device=self.device,
        )

        epsilon = result.epsilon
        cvae_loss = result.final_loss
        metadata = {
            **expected_metadata,
            "epsilon": epsilon,
            "cvae_final_loss": cvae_loss,
            "num_real_examples": len(real_labels),
            "num_synthetic_examples": len(synthetic_labels),
            "real_label_histogram": torch.bincount(
                real_labels.long(), minlength=parameters_federated.NUM_CLASSES
            ).tolist(),
            "synthetic_label_histogram": torch.bincount(
                synthetic_labels.long(), minlength=parameters_federated.NUM_CLASSES
            ).tolist(),
        }
        dp_cvae.save_synthetic_dataset(cache_path, synthetic_embeddings, synthetic_labels, metadata)
        print(
            f"[Cliente {self.partition_id}] Sintéticos salvos em {cache_path}. "
            f"ε_CVAE={epsilon if epsilon is not None else 'sem DP'}"
        )
        synthetic_loader = train.make_embedding_loader(
            synthetic_embeddings,
            synthetic_labels,
            batch_size=parameters_federated.BATCH_SIZE,
            shuffle=True,
        )
        return synthetic_loader, epsilon, cvae_loss, len(synthetic_labels)

    def fit(self, parameters, config):
        # Recebe pesos globais do classificador federado.
        train.set_weights(self.model, parameters)
        self.model.to(self.device)

        # CVAE local gera/carrega embeddings sintéticos privados.
        synthetic_loader, epsilon, cvae_loss, num_synthetic = self._build_or_load_synthetic_loader()

        # FedAvg será feito sobre este classificador treinado nos sintéticos.
        optimizer = torch.optim.Adam(self.model.parameters(), lr=parameters_federated.LR)
        train.train_embedding_classifier(
            self.model,
            synthetic_loader,
            optimizer,
            device=self.device,
            epochs=parameters_federated.EPOCHS,
        )

        metrics = {
            "num_synthetic": float(num_synthetic),
        }
        if epsilon is not None:
            metrics["epsilon-dp-cvae"] = float(epsilon)
        if cvae_loss is not None:
            metrics["cvae_loss"] = float(cvae_loss)

        return train.get_weights(self.model), num_synthetic, metrics

    def evaluate(self, parameters, config):
        train.set_weights(self.model, parameters)
        loss, accuracy = train.test_embedding_classifier_from_images(
            self.model,
            self.embedding_model,
            self.test_loader,
            self.device,
        )
        return loss, len(self.test_loader.dataset), {"accuracy": accuracy}


def client_fn(context: Context):
    partition_id = context.node_config["partition-id"]
    train_loader, test_loader = train.load_data(
        partition_id=partition_id,
        num_partitions=parameters_federated.NUM_PARTITIONS,
    )

    return FlowerClient(
        train_loader,
        test_loader,
        parameters_federated.TARGET_DELTA,
        parameters_federated.NOISE_MULTIPLIER,
        parameters_federated.MAX_GRAD_NORM,
        partition_id,
        context,
    ).to_client()


client_app = ClientApp(client_fn=client_fn)
